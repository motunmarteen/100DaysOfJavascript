//  Create a program that checks if a year is within the 21st century.

let year = prompt("What year do you want to confirm if it is 21st Century?")

if ((year >= 2001) && (year <= 2100)) {
    alert("YES!!! THIS IS A 21st CENTURY YEAR")
} else {
    alert("NO!!! THIS IS NOT A 21st CENTURY YEAR")
}